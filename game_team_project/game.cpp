//********************************************************************
#include "game.h"

// コンストラクタ
Game::Game()
{
    pad_ = 0;
}

// デストラクタ
Game::~Game()
{
}

//********************************************************************
// 初期化
//********************************************************************
bool Game::init()
{
    // メンバ初期化
    if( !field_.init() ) return false;
    if( !player_.init( L"pokemon_char.png" ) )return false;


    return true;
}

//********************************************************************
// 更新
//********************************************************************
bool Game::update()
{
    field_.update( pad_ );
    player_.update( &pad_ );

    return true;
}

//********************************************************************
// 描画
//********************************************************************
void Game::draw()
{
    // レイヤー0,1を描画
    field_.draw();

    // キャラクターを描画
    player_.draw( );

    //// レイヤー2を描画
    //field_.draw();

}

//********************************************************************
// 破棄
//********************************************************************
void Game::destroy()
{
    field_.destroy();
    player_.destroy();
}